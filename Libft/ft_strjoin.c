#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	int		i;
	char	*str;

	if (!s1 || !s2)
		return (NULL);
	str = (char *)malloc((sizeof(char)) * (ft_strlen(s1) + ft_strlen(s2)+ 1));
	if (!str)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	i = 0;
	while (s2[i])
	{
		str[ft_strlen(s1) + i] = s2[i];
		i++;
	}
	str[ft_strlen(s1) + i] = '\0';
	return (str);
}
#include <stdio.h>
int main()
{
	printf("%s\n", ft_strjoin(NULL, "NULL"));
}
